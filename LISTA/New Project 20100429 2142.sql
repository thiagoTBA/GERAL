WHILE NOT vfim DO


    SET vexistecliente = (SELECT COUNT(*)
                          FROM clientestatus
                        WHERE csclicodigo = vcodcli);
  IF vsalario <=1000 THEN
    IF vexistecliente = 1 then
      UPDATE clientestatus SET csstatus = 'CLIENTE NORMAL'
      WHERE csclicodigo = vcodcli;
    ELSE
      INSERT INTO clientestatus values(vcodcli, 'CLIENTE NORMAL');
    END IF;
  ELSE
    IF vexistecliente = 1 then
      UPDATE clientestatus SET csstatus = 'CLIENTE VIP'
      WHERE csclicodigo = vcodcli;
    ELSE
      INSERT INTO clientestatus values(vcodcli, 'CLIENTE VIP');
    END IF;
  END IF;
  SELECT vcodcli, vsalario, vexistecliente;
  FETCH vcursorstatus INTO vcodcli, vsalario;
END WHILE;

CLOSE vcursorstatus;


