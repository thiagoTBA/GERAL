        Date date=null;
        
        SimpleDateFormat formata= new SimpleDateFormat("yyyy-MM-dd");
        try {
            date=formata.parse(request.getParameter("txtdata"));
        } catch (Exception e) {
            JOptionPane.showInputDialog(null, "ERRO FORMATO DATA"+Error.class);
            
        }
        cli.setClinasc(date);
        clidao.salvarcli(cli);
        
        response.sendRedirect("salvarcliente.jsp");
    
    
    
